    /**
     * Salva novo funcionário.
     */
    private void saveNewEmployee() {
		if (!Validator.isEmpty(this.employee)) {
			try {
				this.employee.setGender(Gender.toEnum(String.valueOf(jComboBox1.getSelectedItem())));
				this.employee.setBirthday(jDateChooser2.getDate());
				this.employee.setAdmissionDate(jDateChooser1.getDate());
				this.employee.getAddress().getCity().setName(this.cityName);
				this.employee.getAddress().getCity().getState().setName(String.valueOf(jComboBox2.getSelectedItem()));
				this.employee.getUser().setPassword(PasswordEncrypter.encrypt(this.password));
				this.employee.getUser().setUserName(this.employee.getName());

				this.employeeService.save(this.employee);
                super.showSuccessDialog(this, Messages.SAVE_SUCCESSFULLY + "\n\n" + Messages.EDIT_EMPLOYEE_PERMISSIONS);
				this.clearFields();
			} catch (final CustomMessageException | FieldInvalidException e) {
                super.showWarningDialog(this, e.getMessage());
				logger.save(e.getMessage(), e.getClass().toString(), Constant.NEW_EMPLOYEE_FRAME, "saveNewEmployee");
			} catch (final IOException | NoSuchAlgorithmException e) {
                super.showErrorDialog(this, Messages.UNKNOWN_ERROR);
				logger.save(e.getMessage(), e.getClass().toString(), Constant.NEW_EMPLOYEE_FRAME, "saveNewEmployee");
			}  catch (final ObjectExistsException e) {
                super.showErrorDialog(this, Messages.EMPLOYEE + " " + Messages.ALREADY_EXISTS);
				logger.save(e.getMessage(), e.getClass().toString(), Constant.NEW_EMPLOYEE_FRAME, "saveNewEmployee");
			} catch (final ObjectNotFoundException e) {
                super.showErrorDialog(this, "Cidade " + Messages.NOT_FOUND_FEMALE);
				logger.save(e.getMessage(), e.getClass().toString(), Constant.NEW_EMPLOYEE_FRAME, "saveNewEmployee");
			}
		}
    }
    
